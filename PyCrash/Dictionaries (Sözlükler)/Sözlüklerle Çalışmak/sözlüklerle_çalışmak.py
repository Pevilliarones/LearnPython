# Python'da sözlük bir anahtar-değer çiftleri koleksiyonudur.
# Her anahtar bir değerle bağlantılıdır ve bir anahtarla ilişkili değere erişmek için bu anahtarı kullanabilirsiniz.
# Bir anahtarın değeri bir sayı, bir dizgi, bir liste ve hatta bir başka sözlük olabilir.
# Aslına bakılırsa Python'da oluşturabileceğiniz herhangi bir nesneyi bir sözlükte değer olarak kullanabilirsiniz.

# Python'da sözlük, küme parantezleri ({}) içerisinde sarmalanır.
# Küme parantezleri içerisinde daha önceki örnekte de gösterildiği gibi anahtar-değer çiftleri bulunur:

alien_0 = {"renk": "yeşil", "puan": 5}

# Bir anahtar-değer çifti birbiriyle ilişkili değerler kümesidir.
# Bir anahtar sunduğunuzda Python bu anahtarla ilişkili değeri döndürür.
# Her anahtar değerine iki nokta üst üste işaretiyle bağlanır ve münferit, (tek tek) anahtar değer çiftleri virgülle ayrılır.
# Bir sözlükte istediğiniz kadar anahtar-değer çifti saklayabilirsiniz.

# Aşağıdaki alien_0 sözlüğünün değiştirilmiş versiyonunda da gösterildiği gibi en basit sözlüğün sadece bir tane anahtar-değer çifti vardır:

alien_0 = {"renk": "yeşil"}

# Bu sözlük alien_0 hakkında bir tane bilgi parçasını, yani uzaylının rengini saklar.
# "renk" dizgisi bu sözlükte bir anahtardır ve ilişkili değeri "yeşil"dir.