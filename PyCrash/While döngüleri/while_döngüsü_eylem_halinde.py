# Bir sayı serisi boyunca saymak için while döngüsü kullanabilirsiniz.
# Örneğin aşağıdaki while döngüsü 1'den 5'e kadar sayar.

current_number = 1
while current_number <= 5:
    print(current_number)
    current_number += 1

# İlk satırda current_number'a 1 değerini atayarak saymaya 1'den başlamış oluyoruz.
# Daha sonra while döngüsü current_number 5'e eşit veya 5'ten küçük olduğu sürece çalışacak şekilde ayarlanır.
# Döngünün içerisindeki kod current_number'ın değerini ekrana yazdırır ve current_number+=1 ile bu değere 1 ekler
# (+= operatörü; current_number =  current_number +1 şeklinde yazmanın kısa yoludur.)
